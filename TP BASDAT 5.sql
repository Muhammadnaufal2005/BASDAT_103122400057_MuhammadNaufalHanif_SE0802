SELECT j.ID_THEATER AS NOMOR_TEATER, f.JUDUL AS JUDUL_FILM, SUM(th.HARGA) AS TOTAL_PENDAPATAN
FROM TRANSAKSI t
JOIN JADWAL j ON t.ID_JADWAL = j.ID_JADWAL
JOIN FILM f ON j.ID_FILM = f.ID_FILM
JOIN THEATER th ON j.ID_THEATER = th.ID_THEATER
GROUP BY j.ID_THEATER, f.JUDUL
HAVING SUM(th.HARGA) > 150000;

SELECT * FROM (
    SELECT f.JUDUL AS JUDUL_FILM, COUNT(t.KODE_PEMESANAN) AS TOTAL_TIKET_TERJUAL
    FROM TRANSAKSI t
    JOIN JADWAL j ON t.ID_JADWAL = j.ID_JADWAL
    JOIN FILM f ON j.ID_FILM = f.ID_FILM
    WHERE t.STATUS = 'pesan'
    GROUP BY f.JUDUL
    ORDER BY TOTAL_TIKET_TERJUAL DESC
) WHERE ROWNUM = 1;

SELECT ID_THEATER AS NOMOR_TEATER, AVG(HARGA) AS "AVG(HARGA)"
