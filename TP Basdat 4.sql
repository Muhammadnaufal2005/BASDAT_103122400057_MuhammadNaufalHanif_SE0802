-- 1. HAPUS TABEL LAMA (JIKA ADA) AGAR BERSIH
DROP TABLE TIKET CASCADE CONSTRAINTS;
DROP TABLE KURSI CASCADE CONSTRAINTS;
DROP TABLE TEATER CASCADE CONSTRAINTS;
DROP TABLE FILM CASCADE CONSTRAINTS;
DROP TABLE MEMBER CASCADE CONSTRAINTS;

-- 2. BUAT STRUKTUR TABEL BARU
CREATE TABLE FILM (
    ID_FILM VARCHAR2(10) PRIMARY KEY,
    JUDUL VARCHAR2(100)
);

CREATE TABLE TEATER (
    NOMOR_TEATER VARCHAR2(20),
    ID_FILM VARCHAR2(10),
    HARGA NUMBER,
    PERIODE_START DATE,
    PERIODE_END DATE
);

CREATE TABLE KURSI (
    ID_KURSI VARCHAR2(10),
    NOMOR_TEATER VARCHAR2(20),
    NO_K VARCHAR2(10),
    NO_KURSI VARCHAR2(10)
);

CREATE TABLE MEMBER (
    ID_MEMBER VARCHAR2(10),
    NAMA_MEMBER VARCHAR2(50),
    TGL_LAHIR DATE
);

CREATE TABLE TIKET (
    ID_MEMBER VARCHAR2(10),
    NOMOR_TEATER VARCHAR2(20),
    ID_KURSI VARCHAR2(10),
    TANGGAL DATE,
    STATUS VARCHAR2(20)
);

